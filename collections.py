#Tuples can be of any datatype.
t = (1,"String",0.3345)
print(t)

# You can access elements in tuples just like an array.
print(t[1])

# You can print elements in tuples using a forEach loop in python
print("This is forEach loop")
for arr in t:
    print(arr)

# Tuples can be of multi dimensional array too!
print(type(t))
